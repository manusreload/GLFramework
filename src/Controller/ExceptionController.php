namespace GLFramework\Controller;

/**
 * Class ExceptionController
 *
 * @package GLFramework\Controller
 */
class ExceptionController extends ErrorController
{
    var $trace = null;
    private $exception;
    private $exceptions = array();

    /**
     * ExceptionController constructor.
     * @param $exception \Exception
     */
    public function __construct($exception)
    {
        $error = get_class($exception) . ' ' . $exception->getMessage() . ' at ' . $exception->getFile() . ':';
        $error += $exception->getLine();
        parent::__construct($error);
        $this->exception = $exception;
        $this->trace = $exception->getTraceAsString();
    }

    /**
     * TODO
     *
     * @param \Exception $ex
     * @param array $result
     * @param int $limit
     * @return array
     */
    public function expandException(\Exception $ex, &$result = array(), $limit = 10)
    {
        if ($ex->getPrevious()) {
            if ($limit > 0) {
                $this->expandException($ex, $result, $limit - 1);
            }
        }
        $result[] = $ex;
        return $result;
    }

    /**
     * TODO
     */
    public function run()
    {
        parent::run(); // TODO: Change the autogenerated stub
        $this->exceptions = $this->expandException($this->exception);
    }
}
